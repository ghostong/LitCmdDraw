    private static function stringBreakIn($string, $element) {
        $width = self::elementWidth($element);
        $strWidth = self::stringWidth($string);
        if ($width > $strWidth) {
            $t = ($width - $strWidth) / 2;
            return str_repeat(" ", floor($t)) . $string . PHP_EOL;
        } else {
            return $string . PHP_EOL;
        }

    }

    private static function elementWidth($element) {
        $explode = explode("\n", $element);
        $lens = array_map(function ($str) {
            return self::stringWidth($str);
        }, $explode);
        return max($lens);
    }

    private static function stringWidth($string) {
        return ceil((strlen($string) - mb_strlen($string)) / 2) + mb_strlen($string);
    }
}